# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam.
# o número recebido como parâmetro.


def duplicar(x):
    return x * 2


def triplicar(x):
    return x * 3


def quadruplicar(x):
    return x * 4


if __name__ == '__main__':
    print(duplicar(2))
    print(triplicar(2))
    print(quadruplicar(2))
