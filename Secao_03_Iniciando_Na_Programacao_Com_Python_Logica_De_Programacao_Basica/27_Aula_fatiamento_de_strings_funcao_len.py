"""
Fatiamento de strings
0123456789
Olá mundo
-987654321
Fatiamento [i:f:p] [::]
Obs.: a função len retorna a quantidade de caracteres de uma string
"""

variavel = 'Olá mundo'
print(variavel)
print(variavel[4:])
print(variavel[4:7])
print(variavel[0:5])
print(variavel[:5])
print(variavel[-8:-2])
print(variavel[3])
print(len(variavel[3]))
print(len(variavel))
print(variavel[0:9:1])
print(variavel[0:9:2])
print(variavel[0:9:3])
print(variavel[0:9:4])
print(variavel[0:9:-1])
print(variavel[::-1])
print(variavel[-1:-10:-1])
print(variavel[-1:-10:-2])
