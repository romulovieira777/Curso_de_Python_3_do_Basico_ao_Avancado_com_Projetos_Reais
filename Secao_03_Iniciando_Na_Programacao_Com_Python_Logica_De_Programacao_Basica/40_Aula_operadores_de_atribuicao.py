"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""
contador_01 = 0
contador_01 += 1

print(contador_01)


contador_02 = '1'
contador_02 += '2'

print(contador_02)


contador_03 = 10
contador_03 *= 2

print(contador_03)


contador_04 = 10
contador_04 %= 2
print(contador_04)


contador_05 = 2
contador_05 **= 10
print(contador_05)


contador_06 = 10
contador_06 //= 5

print(contador_06)


contador_07 = 10
contador_07 /= 5

print(contador_07)
