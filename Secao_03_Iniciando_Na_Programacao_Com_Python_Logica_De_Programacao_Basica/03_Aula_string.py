"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
Dinâmica = Não precisa declarar o tipo de variável
Forte = Não permite mudar o tipo de variável
str -> string -> Texto
Strings são textos que estão dentro de aspas simples ou duplas
"""
print(123)

# Aspas simples
print('Olá Mundo do Python!')
print(1, 'Olá Mundo do "Python!"')

# Aspas duplas
print("Olá Mundo do Python!")
print(2, "Olá Mundo do 'Python!'")

# Escape
print("Olá Mundo do \"Python!")
print('Olá Mundo do \'Python!\'')

# r
print(r"Olá Mundo do \"Python!\"")
